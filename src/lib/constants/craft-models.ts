export type ModelType = 'image' | 'video' | 'audio' | 'voice' | 'music';
export type ModelTier = 'fast' | 'quality' | 'ultra';
export type ModelProvider =
  | 'fal' | 'replicate' | 'runway' | 'kling' | 'wan' | 'hailuo' | 'seedance'
  | 'openai' | 'google' | 'stability' | 'suno' | 'elevenlabs' | 'ideogram'
  | 'recraft' | 'qwen' | 'grok' | 'reve' | 'mystic' | 'seedream' | 'zimage';

export interface CraftModel {
  id: string;
  name: string;
  provider: ModelProvider;
  type: ModelType;
  tier: ModelTier;
  badge?: string;                   // e.g. "New", "Recommended", "Best Value"
  description: string;
  keyFeatures: string[];
  maxResolution?: string;
  maxDuration?: number;             // seconds for video/audio
  nativeAudio?: boolean;
  lipSync?: boolean;
  firstFrame?: boolean;
  lastFrame?: boolean;
  motionControl?: boolean;
  realisticOutput?: boolean;
  styleControl?: boolean;
  imageToVideo?: boolean;
  textToVideo?: boolean;
  credits: number;                  // credits per generation
  isRecommended?: boolean;
  isNew?: boolean;
  capabilities: string[];
}

