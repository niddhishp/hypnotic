export const ALL_MODELS = {
  image: IMAGE_MODELS,
  video: VIDEO_MODELS,
  audio: AUDIO_MODELS,
};

export function getModelById(id: string): CraftModel | undefined {
  return [...IMAGE_MODELS, ...VIDEO_MODELS, ...AUDIO_MODELS].find(m => m.id === id);
}

export function getRecommendedModel(type: ModelType): CraftModel | undefined {
  const models = type === 'image' ? IMAGE_MODELS : type === 'video' ? VIDEO_MODELS : AUDIO_MODELS;
  return models.find(m => m.isRecommended) ?? models[0];
}
